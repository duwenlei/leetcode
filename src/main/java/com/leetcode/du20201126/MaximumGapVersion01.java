    /**
     * 超出时间限制
     * @param nums
     * @return
     */
    public static int maximumGap(int[] nums) {
        if (nums.length < 2) {
            return 0;
        }
        quickSort(0, nums.length - 1, nums);
        int length = nums.length;
        int diff = 0;
        for (int i = 0; i < length; i++) {
            if ((i + 1) < length) {
                int tempDiff = nums[i + 1] - nums[i];
                if (tempDiff > diff) {
                    diff = tempDiff;
                }
            }
        }
        return diff;
    }

    public static void quickSort(int left, int right, int[] nums) {
        if (left >= right) {
            return;
        }
        // 基点
        int i = left;
        int j = right;
        int temp = nums[left];
        while (i != j) {
            while (nums[j] >= temp && i < j) {
                j--;
            }
            while (nums[i] <= temp && i < j) {
                i++;
            }
            if (i < j) {
                int t = nums[i];
                nums[i] = nums[j];
                nums[j] = t;
            }
        }
        nums[left] = nums[i];
        nums[i] = temp;
        quickSort(left, i - 1, nums);
        quickSort(i + 1, nums.length - 1, nums);
    }

